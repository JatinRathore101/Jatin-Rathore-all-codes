#include<bits/stdc++.h>
using namespace std;

int main()
{
    int a[10]={0,1,-1,2,-2,3,4,7,7,9};




    cout<<"BINARY RERESENTATIONS"<<endl<<
    "{negative integer is 2's compliment of positive integer}"<<endl<<

    "0           00000000000000000000000000000000"<<endl<<
    "1           00000000000000000000000000000001"<<endl<<
    "-1          11111111111111111111111111111111"<<endl<<
    "2           00000000000000000000000000000010"<<endl<<
    "-2          11111111111111111111111111111110"<<endl<<
    "3           00000000000000000000000000000011"<<endl<<
    "4           00000000000000000000000000000100"<<endl<<
    "7           00000000000000000000000000000111"<<endl<<
    "7           00000000000000000000000000000111"<<endl<<
    "9           00000000000000000000000000001001"<<endl<<endl<<endl;




    for(int i=0;i<9;i++)
    {
        for(int j=i+1;j<10;j++)
        {
            int c=a[i]^a[j];
            cout<<a[i]<<"  ^  "<<a[j]<<"  =  "<<c<<endl;
        }
    }
    return 0;
}

